package model;

public class CasaBonus extends Casa {
	private String mensagemNaCasa;
	private Bonus bonus;

	public String getMensagemNaCasa() {
		return mensagemNaCasa;
	}

	public void setMensagemNaCasa(String mensagemNaCasa) {
		this.mensagemNaCasa = mensagemNaCasa;
	}

	public Bonus getBonus() {
		return bonus;
	}

	public void setBonus(Bonus bonus) {
		this.bonus = bonus;
	}
	
	
	
}
